/*
 * disk/disposable_heroes.c
 *
 * Custom format as used on Disposable Heroes by Gremlin.
 *
 * Written in 2022 by Keith Krellwitz
 *
 * RAW TRACK LAYOUT:
 *  u16 0x4489 :: Sync
 *  u16 0x5555 :: Padding
 *  u16 dat[(768*8)/2+3]
 *
 * The are 3 words after 0x5555, but the loaders never does anything with
 * them and I did could not find any relevance of the data.  
 * 
 * No checksum found.  Added a checksum check to validate the tracks
 * 
 * 
 * TRKTYP_disposable_heroes data layout:
 *  u8 sector_data[0x1800]
 */


#include <libdisk/util.h>
#include <private/disk.h>

static const uint16_t crcs[];

static void *disposable_heroes_write_raw(
    struct disk *d, unsigned int tracknr, struct stream *s)
{
    struct track_info *ti = &d->di->track[tracknr];

    while (stream_next_bit(s) != -1) {
        uint16_t raw[ti->nr_sectors], dat[(ti->len)/2+3];
        unsigned int i;
        char *block;

        if ((uint16_t)s->word != 0x4489)
            continue;
        ti->data_bitoff = s->index_offset_bc - 15;

        if (stream_next_bits(s, 16) == -1)
            goto fail;
        if ((uint16_t)s->word != 0x5555)
            continue;

        stream_start_crc(s);
        for (i = 0; i < ti->bytes_per_sector+3; i++) {
            if (stream_next_bytes(s, raw, 2*ti->nr_sectors) == -1)
                break;
            mfm_decode_bytes(bc_mfm_even_odd, ti->nr_sectors, raw, &dat[i*4]);
        }

        if (s->crc16_ccitt != crcs[tracknr] && s->crc16_ccitt != crcs[tracknr+160])
            goto fail;

        stream_next_index(s);
        ti->total_bits = s->track_len_bc;
        block = memalloc(ti->len+6);
        memcpy(block, dat, ti->len+6);
        set_all_sectors_valid(ti);
        return block;
    }

fail:
    return NULL;
}

static void disposable_heroes_read_raw(
    struct disk *d, unsigned int tracknr, struct tbuf *tbuf)
{
    struct track_info *ti = &d->di->track[tracknr];
    uint16_t *dat = (uint16_t *)ti->dat;
    unsigned int i;

    tbuf_bits(tbuf, SPEED_AVG, bc_raw, 16, 0x4489);
    tbuf_bits(tbuf, SPEED_AVG, bc_raw, 16, 0x5555);

    for (i = 0; i < ti->bytes_per_sector+3; i++) {
        tbuf_bytes(tbuf, SPEED_AVG, bc_mfm_even_odd, ti->nr_sectors, &dat[i*4]);
    }
}

struct track_handler disposable_heroes_handler = {
    .bytes_per_sector = 768,
    .nr_sectors = 8,
    .write_raw = disposable_heroes_write_raw,
    .read_raw = disposable_heroes_read_raw
};

